export const SUPPORTED_LOCALES = ["en", "zh"] as const;
export type AppLocale = (typeof SUPPORTED_LOCALES)[number];

export const LOCALE_COOKIE_NAME = "lang";

const CHINESE_COUNTRIES = new Set(["CN", "HK", "MO", "TW", "SG"]);

function normalizeLocale(input: string | null | undefined): AppLocale | null {
  if (!input) return null;
  const lowered = input.toLowerCase();
  if (lowered.startsWith("zh")) return "zh";
  if (lowered.startsWith("en")) return "en";
  return null;
}

export function inferLocale(input: {
  cookieLocale?: string | null;
  acceptLanguage?: string | null;
  country?: string | null;
}): AppLocale {
  const fromCookie = normalizeLocale(input.cookieLocale);
  if (fromCookie) return fromCookie;

  const country = input.country?.toUpperCase() ?? "";
  if (CHINESE_COUNTRIES.has(country)) return "zh";

  const acceptLanguage = input.acceptLanguage ?? "";
  const fromHeader = acceptLanguage
    .split(",")
    .map((part) => normalizeLocale(part.trim()))
    .find(Boolean);

  return fromHeader ?? "en";
}

